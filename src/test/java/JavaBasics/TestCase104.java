package JavaBasics;

public class TestCase104 {
	
	public static void main(String[] args)
	{
		
		for(int i=0;i<=7;i++)
		{
			
			for(int j=0;j<=9;j++)
			{
				if(i==0 || j==0 || i==7 || j==9)
				{
				System.out.print("*");
				}
				else
				{
					System.out.print(" ");
				}
				
			}
			System.out.println();
		
			
		}
	
		

	}
	
	
	
	
	

}
